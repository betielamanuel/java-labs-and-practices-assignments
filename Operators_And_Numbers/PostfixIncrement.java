package Operators_And_Numbers;

public class PostfixIncrement {
    public static void main(String[] args) {
        
    int number = 5;

    System.out.println("Before increment: " + number); // Print the value before incrementing
    number++; // Postfix increment operator
    System.out.println("After increment: " + number); // Print the value after incrementing
    }
}
